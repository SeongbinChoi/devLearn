package com.sp.dev.mypage.calendar;


public class Calendar {
	private int planNum;
	private String memberEmail;
	private String subject;
	private String planColor;
	private String sDate;
	private String eDate;
	private int repeatCheck;
	private int repeatCycle;
	private String memo;
	private String regDate;
	
	
	public int getPlanNum() {
		return planNum;
	}
	public void setPlanNum(int planNum) {
		this.planNum = planNum;
	}
	public String getMemberEmail() {
		return memberEmail;
	}
	public void setMemberEmail(String memberEmail) {
		this.memberEmail = memberEmail;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getPlanColor() {
		return planColor;
	}
	public void setPlanColor(String planColor) {
		this.planColor = planColor;
	}
	public String getsDate() {
		return sDate;
	}
	public void setsDate(String sDate) {
		this.sDate = sDate;
	}
	public String geteDate() {
		return eDate;
	}
	public void seteDate(String eDate) {
		this.eDate = eDate;
	}
	public int getRepeatCheck() {
		return repeatCheck;
	}
	public void setRepeatCheck(int repeatCheck) {
		this.repeatCheck = repeatCheck;
	}
	public int getRepeatCycle() {
		return repeatCycle;
	}
	public void setRepeatCycle(int repeatCycle) {
		this.repeatCycle = repeatCycle;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	
	

}
