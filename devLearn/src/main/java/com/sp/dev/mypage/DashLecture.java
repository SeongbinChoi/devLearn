package com.sp.dev.mypage;

public class DashLecture {
	private String lectureSubject;
	private String lectureSdate;
	
	private int totalLectureNum;
	private int finishLectureNum;
	
	public int getTotalLectureNum() {
		return totalLectureNum;
	}
	public void setTotalLectureNum(int totalLectureNum) {
		this.totalLectureNum = totalLectureNum;
	}
	public int getFinishLectureNum() {
		return finishLectureNum;
	}
	public void setFinishLectureNum(int finishLectureNum) {
		this.finishLectureNum = finishLectureNum;
	}
	public String getLectureSubject() {
		return lectureSubject;
	}
	public void setLectureSubject(String lectureSubject) {
		this.lectureSubject = lectureSubject;
	}
	public String getLectureSdate() {
		return lectureSdate;
	}
	public void setLectureSdate(String lectureSdate) {
		this.lectureSdate = lectureSdate;
	}
	
	
}
