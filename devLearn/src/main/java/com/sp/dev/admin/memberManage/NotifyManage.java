package com.sp.dev.admin.memberManage;

public class NotifyManage {
	private int notifyNum;
	private int qnaNum;
	private String notifyReason;
	private String notifierName;
	private String notifiedName;
	private String notifierEmail;
	private String notifiedEmail;
	private String subject;
	private String state;
	private String regDate;
	private String updateDate;
	
	public int getNotifyNum() {
		return notifyNum;
	}
	public void setNotifyNum(int notifyNum) {
		this.notifyNum = notifyNum;
	}
	public int getQnaNum() {
		return qnaNum;
	}
	public void setQnaNum(int qnaNum) {
		this.qnaNum = qnaNum;
	}
	public String getNotifyReason() {
		return notifyReason;
	}
	public void setNotifyReason(String notifyReason) {
		this.notifyReason = notifyReason;
	}
	public String getNotifierName() {
		return notifierName;
	}
	public void setNotifierName(String notifierName) {
		this.notifierName = notifierName;
	}
	public String getNotifiedName() {
		return notifiedName;
	}
	public void setNotifiedName(String notifiedName) {
		this.notifiedName = notifiedName;
	}
	public String getNotifierEmail() {
		return notifierEmail;
	}
	public void setNotifierEmail(String notifierEmail) {
		this.notifierEmail = notifierEmail;
	}
	public String getNotifiedEmail() {
		return notifiedEmail;
	}
	public void setNotifiedEmail(String notifiedEmail) {
		this.notifiedEmail = notifiedEmail;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	
}
